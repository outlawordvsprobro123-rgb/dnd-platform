export async function POST() {
  const supabase = await createServerClient()
  const { data: { user } } = await supabase.auth.getUser()

  if (!user) return NextResponse.json({ error: 'Не авторизован' }, { status: 401 })

  const { data: sessions } = await supabase
    .from('sessions').select('id').eq('master_id', user.id).limit(1)

  if (!sessions || sessions.length === 0)
    return NextResponse.json({ error: 'Только для Мастера' }, { status: 403 })

  const results: Record<string, unknown> = {}

  const creatureNames = CREATURES.map(c => c.name)
  const lootNames = LOOT_ITEMS.map(i => i.name)
  const spellNames = SPELLS.map(s => s.name)

  // Удаляем старые записи с теми же именами (чтобы не было дублей)
  await supabase.from('bestiary').delete().in('name', creatureNames).eq('is_homebrew', true)
  await supabase.from('loot_items').delete().in('name', lootNames).eq('is_homebrew', true)
  await supabase.from('spells').delete().in('name', spellNames).eq('is_homebrew', true)

  // --- Бестиарий ---
  const creaturesWithUser = CREATURES.map(c => ({ ...c, created_by: user.id }))
  const { data: creatureData, error: creatureError } = await supabase
    .from('bestiary').insert(creaturesWithUser).select('name')

  if (creatureError) {
    console.error('Bestiary error:', creatureError)
    results.creatures = { error: creatureError.message }
  } else {
    results.creatures = { inserted: creatureData?.length ?? 0 }
  }

  // --- Лут ---
  const lootWithUser = LOOT_ITEMS.map(i => ({ ...i, created_by: user.id }))
  const { data: lootData, error: lootError } = await supabase
    .from('loot_items').insert(lootWithUser).select('name')

  if (lootError) {
    console.error('Loot error:', lootError)
    results.loot = { error: lootError.message }
  } else {
    results.loot = { inserted: lootData?.length ?? 0 }
  }

  // --- Заклинания ---
  const spellsWithUser = SPELLS.map(s => ({ ...s, created_by: user.id }))
  const { data: spellData, error: spellError } = await supabase
    .from('spells').insert(spellsWithUser).select('name')

  if (spellError) {
    console.error('Spells error:', spellError)
    results.spells = { error: spellError.message }
  } else {
    results.spells = { inserted: spellData?.length ?? 0 }
  }

  return NextResponse.json({ success: true, results })
}
